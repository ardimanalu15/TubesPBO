        private void tampilkan_data(){
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("Nama");
            model.addColumn("Harga_Buah");
            model.addColumn("ID_Distributor");
            model.addColumn("expired");
            model.addColumn("Daerah");
            model.addColumn("Jenis");
            model.addColumn("Kualitas");               
            model.addColumn("NO_Transaksi");
            model.addColumn("NO_Penyimpanan");
            model.addColumn("TANGGAL");
            model.addColumn("STOK");
          
            try{
                int no = 1;
                String sql = "SELECT * FROM tabel_buah";
                java.sql.Connection koneksi = (Connection)Toko_Buah.tokobuahDB();
                java.sql.Statement stm = koneksi.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                
                while(res.next()){
                    model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10), res.getString(11)});
                    
                }
                tabelbuah.setModel(model);
                
                
            }catch(SQLException e) {
                System.out.println("Error : " + e.getMessage());
                
            }
            
            
        }
        
        
