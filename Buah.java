        txtharga_pokok.setText(harga_buah);
        
        String id_distributor = tabelbuah.getValueAt(baris, 3).toString();
        txtid_distributor.setText(id_distributor);
        
        String expired = tabelbuah.getValueAt(baris, 4).toString();
        txtexpired.setText(expired);
        
        String daerah = tabelbuah.getValueAt(baris, 5).toString();
        txtdaerah.setText(daerah);
        
        String jenis = tabelbuah.getValueAt(baris, 6).toString();
        txtjenis.setText(jenis);
        
        String kualitas = tabelbuah.getValueAt(baris, 7).toString();
        txtkualitas.setText(kualitas);        
               
        String no_transaksi = tabelbuah.getValueAt(baris, 8).toString();
        txtno_transaksi.setText(no_transaksi);
        
        String no_penyimpanan = tabelbuah.getValueAt(baris,9).toString();
        txtno_penyimpanan.setText(no_penyimpanan); 
        
        String tanggal = tabelbuah.getValueAt(baris, 10).toString();
        txttanggal.setText(tanggal);
        
         String stok = tabelbuah.getValueAt(baris,11).toString();
        txtstok_buah.setText(stok); 
        
