    private void tabelkaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelkaryawanMouseClicked
        // TODO add your handling code here:
         int baris = tabelkaryawan.rowAtPoint(evt.getPoint());
        String id_karyawan = tabelkaryawan.getValueAt(baris, 1).toString();
        txtid_karyawan.setText(id_karyawan);
        
        String nama = tabelkaryawan.getValueAt(baris, 2).toString();
        txtnama.setText(nama);
        
        String alamat = tabelkaryawan.getValueAt(baris, 3).toString();
        txtalamat.setText(alamat);
        
        String gaji = tabelkaryawan.getValueAt(baris, 4).toString();
        txtgaji.setText(gaji);
        
        String umur = tabelkaryawan.getValueAt(baris, 5).toString();
        txtumur.setText(umur);
        
        String jenis_kelamin = tabelkaryawan.getValueAt(baris, 6).toString();
        txtjenis_kelamin.setText(jenis_kelamin);      
    }//GEN-LAST:event_tabelkaryawanMouseClicked

