     private void tampilkan_data(){
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("ID_Karyawan");
            model.addColumn("Nama");
            model.addColumn("Alamat");
            model.addColumn("Gaji");
            model.addColumn("Umur");
            model.addColumn("Jenis_kelamin");
            
            try{
                int no = 1;
                String sql = "SELECT * FROM karyawan";
                java.sql.Connection koneksi = (Connection)Toko_Buah.tokobuahDB();
                java.sql.Statement stm = koneksi.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                
                while(res.next()){
                    model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6)});
                    
                }
                tabelkaryawan.setModel(model);
                
                
            }catch(SQLException e) {
                System.out.println("Error : " + e.getMessage());
                
            }
            
            
        }
    
    
