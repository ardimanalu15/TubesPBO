    private void tbsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbsimpanActionPerformed
        // TODO add your handling code here:
         try{
            String sql = "INSERT INTO karyawan VALUES ('"+txtid_karyawan.getText()+"','" +txtnama.getText()+"','" +txtalamat.getText()+"','" +txtgaji.getText()+"','" +txtumur.getText()+"','" +txtjenis_kelamin.getText()+"')";
            
            java.sql.Connection koneksi = (Connection)Toko_Buah.tokobuahDB();
            java.sql.PreparedStatement pstm = koneksi.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil...");
            tampilkan_data();
            kosongkan_form();
            
            
        }catch ( HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this , e.getMessage());
            
        }
    }//GEN-LAST:event_tbsimpanActionPerformed

